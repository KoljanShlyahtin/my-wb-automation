    var firstCorridorChannel = GRADUAL_BRIGHTNESS_CHANNELS[0];
    if (dev[firstCorridorChannel] === undefined) {
        log("‚ùå setGradualBrightnessChannels: –∫–∞–Ω–∞–ª {} –Ω–µ –Ω–∞–π–¥–µ–Ω. –ü—Ä–∏–º–µ–Ω—è–µ–º –æ–±—ã—á–Ω—É—é —É—Å—Ç–∞–Ω–æ–≤–∫—É —è—Ä–∫–æ—Å—Ç–∏.", firstCorridorChannel);
        setBrightnessChannels(targetValue);
        return;
    }

    var currentBrightness = dev[firstCorridorChannel];
    log("üîÑ setGradualBrightnessChannels: —Ç–µ–∫—É—â–∞—è —è—Ä–∫–æ—Å—Ç—å –Ω–∞ {}: {}", firstCorridorChannel, currentBrightness);

    if (currentBrightness === targetValue) {
        log("‚ÑπÔ∏è setGradualBrightnessChannels: —Ç–µ–∫—É—â–∞—è —è—Ä–∫–æ—Å—Ç—å ({}) —É–∂–µ —Ä–∞–≤–Ω–∞ —Ü–µ–ª–µ–≤–æ–π ({}). –ü–ª–∞–≤–Ω—ã–π –ø–µ—Ä–µ—Ö–æ–¥ –Ω–µ —Ç—Ä–µ–±—É–µ—Ç—Å—è.", currentBrightness, targetValue);
        return;
    }

    // –ü–∞—Ä–∞–º–µ—Ç—Ä—ã –ø–ª–∞–≤–Ω–æ–≥–æ –ø–µ—Ä–µ—Ö–æ–¥–∞
    var stepSize = 1; // –ò–∑–º–µ–Ω–µ–Ω–∏–µ —è—Ä–∫–æ—Å—Ç–∏ –Ω–∞ 1% –∑–∞ —à–∞–≥
    var stepIntervalMs = 4000; // –ò–Ω—Ç–µ—Ä–≤–∞–ª –º–µ–∂–¥—É —à–∞–≥–∞–º–∏ –≤ –º–∏–ª–ª–∏—Å–µ–∫—É–Ω–¥–∞—Ö (5 —Å–µ–∫—É–Ω–¥)

